/**
 * - sebelumnya kita pernah menggunakan kata kunci final di Java
 * - jika digunakan di variable, maka variable tersebut tidak bisa berubah lagi datanya
 * - final pun bisa digunakan di class, dimana jika menggunakan kata kunci final sebelum class, maka itu menandakan bahwa
 * class tersebut tidak bisa diwariskan lagi
 * - secara otomatis semua child class akan eror
 */

public class Main {
    public static void main(String[] args) {
        System.out.println("Hello, World!");
    }
}